package Cadastro;

import javax.swing.Spring;

public class Estado {
    
    private Spring nome;
    private Spring sigla;
    
    public Spring getNome() {
        return nome;
    }
    public void setNome(Spring nome) {
        this.nome = nome;
    }
    public Spring getSigla() {
        return sigla;
    }
    public void setSigla(Spring sigla) {
        this.sigla = sigla;
    }
    
}
